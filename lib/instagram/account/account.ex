defmodule Instagram.Account do

  import Ecto.Query, warn: false
  alias Instagram.Repo

  alias Instagram.Account.User

  def photos_for_user(user) do
    Ecto.assoc(user, :photos)
    |> Repo.all
  end

  def list_users do
    Repo.all(User)
  end

  def get_user!(id), do: Repo.get!(User, id)

  def create_user(attrs \\ %{}) do
    %User{}
    |> User.changeset(attrs)
    |> Repo.insert()
  end

  def update_user(%User{} = user, attrs) do
    user
    |> User.changeset(attrs)
    |> Repo.update()
  end

  def delete_user(%User{} = user) do
    Repo.delete(user)
  end

  def change_user(%User{} = user) do
    User.changeset(user, %{})
  end
end
